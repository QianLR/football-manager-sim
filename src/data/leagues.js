export function getLeagueRoster(leagueId) {
  const roster = LEAGUE_ROSTERS[leagueId];
  return Array.isArray(roster) ? roster : null;
}

export function getLeagueSize(leagueId) {
  const roster = getLeagueRoster(leagueId);
  return roster ? roster.length : null;
}

export function getUclSeedPool() {
  const leagues = Object.keys(LEAGUE_ROSTERS);
  const pool = [];
  leagues.forEach(id => {
    const roster = LEAGUE_ROSTERS[id] || [];
    roster.slice(0, 4).forEach(team => {
      pool.push({ ...team, leagueId: id });
    });
  });
  return pool;
}
